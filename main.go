func createMemQuota(serviceName, namespace string, dimensions map[string]string) *model.StructMemQuota {
	paramsQuota := &config.Params_Quota{
		Name:          fmt.Sprintf("memquota-quota-%s", serviceName),
		MaxAmount:     1000,
		ValidDuration: time.Second,
		Overrides: []config.Params_Override{
			{
				Dimensions:    dimensions,
				MaxAmount:     1,
				ValidDuration: time.Second * 5,
			},
		},
	}
	memquotaName := fmt.Sprintf("handler-memquota-%s", serviceName)

	memquota := model.MemQuota(memquotaName, namespace, []*config.Params_Quota{paramsQuota})
	return memquota
}

func createQuotaJsonObj(quotaName, namespace string, dimensions map[string]string) map[string]interface{} {
	/* Sample:
	   {
	     "apiVersion": "config.istio.io/v1alpha2",
	     "kind": "quota",
	     "metadata": {
	       "clusterName": "",
	       "name": "requestcount",
	       "namespace": "istio-system",
	     },
	     "spec": {
	       "dimensions": {
	         "destination": "destination.labels[\"app\"] | destination.service | \"unknown\"",
	         "destinationVersion": "destination.labels[\"version\"] | \"unknown\"",
	         "source": "source.labels[\"app\"] | source.service | \"unknown\"",
	         "sourceVersion": "source.labels[\"version\"] | \"unknown\""
	       }
	     }
	   }
	*/

	metadata := map[string]string{
		"name":      quotaName,
		"namespace": namespace,
	}

	quota := map[string]interface{}{
		"apiVersion": "config.istio.io/v1alpha2",
		"kind":       "quota",
		"metadata":   metadata,
		"spec": map[string]interface{}{
			"dimensions": dimensions,
		},
	}

	return quota
}

